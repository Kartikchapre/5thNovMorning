package kartik;

public class greaterThanLessThan {
	public static void main(String[] args) {
		
		int a=50, b=60;
		
		System.out.println(a>b); //greater than
		
		System.out.println(a<b); //less than
		
		System.out.println(a<=b); //less than equals too
		
		System.out.println(a>=b); //greater than equals too
		
		System.out.println(a==b); //== operator
		
		System.out.println(a!=b); //!= not equals too
		
	}

}
