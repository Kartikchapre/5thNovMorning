package kartik;

public class ANDOperator {
	{
	// && operators ( AND )
	
	System.out.println(( 10>3 ) && ( 12>7 )); // true
	
	System.out.println(( 10>3 ) && ( 12<7 )); //false
	
	// or operator ||
	
	System.out.println((10>3 ) || (12<7)); //true
	
	System.out.println((10<3 ) || (12<7)); //false
	
	// not operator !
	
	System.out.println(!(10==7)); //true
	System.out.println(!(5>11)); //false
	
	
	

	}
	}
